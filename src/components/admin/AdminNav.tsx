interface AdminNavProps {
  forceOpen?: boolean
  onClose?: () => void
  permissions?: string[]
}

export function AdminNav({ forceOpen, onClose, permissions = [] }: AdminNavProps = {}) {
  const pathname = usePathname()
  const [isOpen, setIsOpen] = useState(false)
  const [expandedSections, setExpandedSections] = useState<string[]>(['POS', 'Menu', 'Reports'])

  // Check if user has permission to view an item
  const canView = (permission?: string) => {
    if (!permission) return true
    // Everyone can access POS routes
    if (permission === 'pos.access') return true
    // Check specific permission or admin access
    return hasPermission(permissions, permission)
  }

  // Filter nav sections and items based on permissions
  const filteredNavSections = navSections
    .map(section => ({
      ...section,
      items: section.items.filter(item => canView(item.permission)),
    }))
    .filter(section => {
      // Show section if it has visible items OR user has section permission
      const hasVisibleItems = section.items.length > 0
      const hasAccess = !section.permission || canView(section.permission)
      return hasVisibleItems && hasAccess
    })

  // Use forceOpen prop if provided, otherwise use internal state
  const isVisible = forceOpen !== undefined ? forceOpen : isOpen

  const toggleSection = (title: string) => {
    setExpandedSections(prev =>
      prev.includes(title)
        ? prev.filter(s => s !== title)
        : [...prev, title]
    )
  }

  const isActive = (href: string) => pathname === href

  const handleClose = () => {
    if (onClose) {
      onClose()
    } else {
      setIsOpen(false)
    }
  }

  return (
    <>
      {/* Mobile menu button - only show if not controlled externally */}
      {forceOpen === undefined && (
        <button
          onClick={() => setIsOpen(!isOpen)}
          className="fixed top-4 left-4 z-50 lg:hidden bg-white p-2 rounded-lg shadow-md"
        >
          <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            {isVisible ? (
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            ) : (
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
            )}
          </svg>
        </button>
      )}

      {/* Overlay - only for internal mobile menu, not when force controlled */}
      {forceOpen === undefined && isVisible && (
        <div
          className="fixed inset-0 bg-black/30 z-40 lg:hidden"
          onClick={handleClose}
        />
      )}

      {/* Sidebar */}
      <nav
        className={`fixed top-0 left-0 bottom-0 w-64 bg-white shadow-lg z-50 transform transition-transform ${
          forceOpen !== undefined
            ? (isVisible ? 'translate-x-0' : '-translate-x-full')
            : `lg:translate-x-0 ${isVisible ? 'translate-x-0' : '-translate-x-full'}`
        }`}
      >
        <div className="h-full flex flex-col">
          {/* Logo */}
          <div className="p-4 border-b">
            <h1 className="text-xl font-bold text-gray-800">GWI POS</h1>
            <p className="text-xs text-gray-500">Admin Dashboard</p>
          </div>

          {/* Navigation */}
          <div className="flex-1 overflow-y-auto py-4">
            {filteredNavSections.map(section => (
              <div key={section.title} className="mb-2">
                <button
                  onClick={() => toggleSection(section.title)}
                  className="w-full px-4 py-2 flex items-center justify-between text-sm font-semibold text-gray-600 hover:bg-gray-50"
                >
                  <span>{section.title}</span>
                  <svg
                    className={`w-4 h-4 transition-transform ${
                      expandedSections.includes(section.title) ? 'rotate-180' : ''
                    }`}
                    fill="none"
                    stroke="currentColor"
                    viewBox="0 0 24 24"
                  >
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                  </svg>
                </button>

                {expandedSections.includes(section.title) && (
                  <div className="mt-1">
                    {section.items.map(item => (
                      <Link
                        key={item.href}
                        href={item.href}
                        onClick={handleClose}
                        className={`flex items-center gap-3 px-6 py-2 text-sm transition-colors ${
                          isActive(item.href)
                            ? 'bg-blue-50 text-blue-700 border-r-2 border-blue-700'
                            : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                        }`}
                      >
                        {item.icon}
                        <span>{item.name}</span>
                      </Link>
                    ))}
                  </div>
                )}
              </div>
            ))}
          </div>

          {/* Footer */}
          <div className="p-4 border-t text-xs text-gray-500">
            <p>Version 1.0.0</p>
          </div>
        </div>
      </nav>
    </>
  )
}
