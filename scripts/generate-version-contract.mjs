  'int4': 'INTEGER',
  'int8': 'BIGINT',
  'int2': 'SMALLINT',
  'bool': 'BOOLEAN',
  'float4': 'REAL',
  'float8': 'DOUBLE PRECISION',
  'numeric': 'DECIMAL',
  'text': 'TEXT',
  'varchar': 'VARCHAR',
  'timestamp': 'TIMESTAMP',
  'timestamptz': 'TIMESTAMPTZ',
  'jsonb': 'JSONB',
  'json': 'JSON',
  'uuid': 'UUID',
  'bytea': 'BYTEA',
  'serial': 'SERIAL',
  'bigserial': 'BIGSERIAL',
  'smallserial': 'SMALLSERIAL',
  'date': 'DATE',
  'time': 'TIME',
  'timetz': 'TIMETZ',
  'interval': 'INTERVAL',
  'cidr': 'CIDR',
