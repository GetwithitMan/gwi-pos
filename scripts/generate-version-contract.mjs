/**
 * Generate version contract from migration state + live DB schema introspection.
 * Runs at build time alongside generate-schema-sql.mjs.
 * Output is git-committed and consumed by both POS and MC.
 *
 * The `installerVersion` field is used by installer.run's self-update check
 * to determine if a newer installer is available on Vercel.
 *
 * Component versions track ALL deployable artifacts so the update agent,
 * installer, and dashboard can detect when any component needs updating.
 *
 * Phase 1A additions (schema drift elimination):
 *  - Full schema contract via DB introspection (tables, columns, indexes, enums, FKs)
 *  - Canonical structure with deterministic hashing (schemaContractHash)
 *  - Risk classification by diffing against previous contract
 *  - Compatibility analysis (forward_compatible, requires_schema_first, etc.)
 *  - Optional HMAC-SHA256 signing via SCHEMA_CONTRACT_SECRET env var
 */
import { readdirSync, readFileSync, writeFileSync, existsSync } from 'fs'
import { execSync } from 'child_process'
import { createHash, createHmac } from 'crypto'
import path from 'path'

const root = process.cwd()
const migrationsDir = path.join(root, 'scripts/migrations')
const outPath = path.join(root, 'src/generated/version-contract.json')

// ── App version from package.json ─────────────────────────────────────────
const pkg = JSON.parse(readFileSync(path.join(root, 'package.json'), 'utf-8'))
const appVersion = pkg.version // e.g. "1.1.1357"

// ── Git SHA ───────────────────────────────────────────────────────────────
let gitSha = 'unknown'
try {
  gitSha = execSync('git rev-parse --short HEAD', { encoding: 'utf-8' }).trim()
} catch { /* not in a git repo — use fallback */ }

// Scan for highest NNN prefix
const files = readdirSync(migrationsDir).filter(f => /^\d{3}-/.test(f))
const versions = files.map(f => f.match(/^(\d{3})/)?.[1]).filter(Boolean).sort()
const schemaVersion = versions[versions.length - 1] || '000'

// Read schema hash from sidecar file (generated by generate-schema-sql.mjs)
const hashPath = path.join(root, 'prisma/schema-hash.txt')
const schemaSha256 = existsSync(hashPath) ? readFileSync(hashPath, 'utf-8').trim() : null

// ── Component versions ────────────────────────────────────────────────────
// Dashboard version — extracted from bundled .deb or fallback version file
const dashboardVersion = (() => {
  try {
    // Method 1: Extract version from bundled .deb
    const debPath = path.join(root, 'public/gwi-nuc-dashboard.deb')
    if (existsSync(debPath)) {
      try {
        const ver = execSync(`dpkg-deb -f "${debPath}" Version 2>/dev/null`, { encoding: 'utf8' }).trim()
        if (ver) return ver
      } catch { /* dpkg-deb not available (macOS) — fall through */ }
    }
    // Method 2: Read from maintained version file
    const versionFile = path.join(root, 'public/dashboard-version.txt')
    if (existsSync(versionFile)) {
      return readFileSync(versionFile, 'utf8').trim()
    }
    return '0.1.0'
  } catch { return '0.1.0' }
})()

// Ansible baseline version — read from installer manifest
const ansibleBaselineVersion = (() => {
  try {
    const manifestPath = path.join(root, 'installer/version.txt')
    if (existsSync(manifestPath)) {
      return readFileSync(manifestPath, 'utf8').trim()
    }
    return '2026.03.20.1'
  } catch { return '2026.03.20.1' }
})()

// ── Type normalization map ────────────────────────────────────────────────
